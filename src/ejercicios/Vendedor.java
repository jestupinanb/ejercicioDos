package ejercicios;

/**
 *
 * @author Estudiante
 */
public class Vendedor {
    int id;
    String nombre;
    String apellido;

    public Vendedor(int id, String nombre, String apellido) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public String getApellido() {
        return apellido;
    }
    
}
